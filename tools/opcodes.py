"""Opcode constants and instruction-length metadata for miniZ80."""

ROM_ORIGIN = 0x0000

# 1-byte opcodes used in milestone and common ops
NOP = 0x00
HALT = 0x76
LD_A_N = 0x3E

THREE_BYTE = {0x01, 0x11, 0x21, 0x31, 0xC3, 0xCD, 0xC4, 0xCC, 0xD4, 0xDC}
TWO_BYTE = {
    0x06, 0x0E, 0x16, 0x1E, 0x26, 0x2E, 0x36, 0x3E,
    0x10, 0x18, 0x20, 0x28, 0x30, 0x38,
    0xC6, 0xD6, 0xE6, 0xEE, 0xF6, 0xFE,
}


def build_valid_opcodes() -> set[int]:
  one = set(range(0x40, 0x80))
  one |= {
      0x04, 0x0C, 0x14, 0x1C, 0x24, 0x2C, 0x34, 0x3C,
      0x05, 0x0D, 0x15, 0x1D, 0x25, 0x2D, 0x35, 0x3D,
      0x03, 0x13, 0x23, 0x33, 0x0B, 0x1B, 0x2B, 0x3B,
      0x00, 0x07, 0x0F, 0x17, 0x1F, 0x37, 0x3F,
      0xC1, 0xC5, 0xD1, 0xD5, 0xE1, 0xE5, 0xF1, 0xF5,
      0xC0, 0xC8, 0xC9, 0xD0, 0xD8,
  }
  for base in (0x80, 0x90, 0xA0, 0xA8, 0xB0, 0xB8):
      for s in range(8):
          one.add(base + s)
  one -= TWO_BYTE
  one -= THREE_BYTE
  return one | TWO_BYTE | THREE_BYTE


VALID_OPCODES = build_valid_opcodes()


def insn_length(op: int) -> int:
  if op in THREE_BYTE:
      return 3
  if op in TWO_BYTE:
      return 2
  if op in VALID_OPCODES:
      return 1
  return 0
